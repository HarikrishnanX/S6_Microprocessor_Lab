ASSUME CS:CODE,DS:DATA

DATA SEGMENT
MSG1 DB 0AH,"Enter 1st Number:$"
MSG2 DB 0AH,"Enter 2nd Number:$"
RESULT DB 0AH,0DH,"The Product is:$"
DATA ENDS

CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	
	MOV BX,0
START1:MOV AH,01H
	INT 21H
	
	CMP AL,0DH
	JZ NEXT1
	MOV AH,0
	SUB AL,30H
	PUSH AX
	
	MOV AX,10D
	MUL BX
	POP BX
	ADD BX,AX
	
	JMP START1
NEXT1:	PUSH BX
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	MOV BX,0
START2:MOV AH,01H
	INT 21H
	
	CMP AL,0DH
	JZ NEXT2
	MOV AH,0
	SUB AL,30H
	PUSH AX
	MOV AX,10D
	MUL BX
	POP BX
	ADD BX,AX
	JMP START2
	
NEXT2:	POP AX
	MUL BX
	PUSH AX
	
	LEA DX,RESULT
	MOV AH,09H
	INT 21H
	
	POP AX
	MOV CX,0
	MOV DX,0
	MOV BX,10D
	
	BREAK:	DIV BX
		PUSH DX
		MOV DX,0
		INC CX
		OR AX,AX
		JNZ BREAK

	PRINT:	POP DX
		ADD DL,30H
		MOV AH,02H
		INT 21H
		LOOP PRINT

	MOV AH,4CH
	INT 21H	
CODE ENDS
END START
