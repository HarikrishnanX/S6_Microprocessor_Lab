ASSUME CS:CODE,DS:DATA

DISPLAY MACRO MSG
LEA DX,MSG
MOV AH,09H
INT 21H
ENDM

DATA SEGMENT
MSG1 DB 0AH,"Enter the String:$"
MSG2 DB 0AH,0DH,"The Length is:$"
DATA ENDS

CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX

	DISPLAY MSG1
	
	MOV BX,0
READ:	MOV AH,01H
	INT 21H
	
	CMP AL,0DH
	JZ PRINTL
	INC BX
	JMP READ
PRINTL:	DISPLAY MSG2
	MOV AX,BX
	MOV CX,0
	MOV DX,0
	MOV BX,10D
	BREAK:	DIV BX
		PUSH DX
		MOV DX,0
		INC CX
		OR AX,AX
		JNZ BREAK
	PRINT:	POP DX
		ADD DL,30H
		
		MOV AH,02H
		INT 21H
	
		LOOP PRINT

	MOV AH,4CH
	INT 21H			
CODE ENDS
END START